from gi.repository import Gtk, Gio

class TravelApp(Gtk.Application):

    def __init__(self):
        super().__init__(application_id="TravelApp")
        self.connect("activate", self.on_activate)

    def on_activate(self, app):
        # Создаем главное окно
        window = Gtk.ApplicationWindow(application=app)
        window.set_title("Путеводитель по городам России")
        window.set_default_size(800, 600)

        # Создаем основной контейнер
        main_box = Gtk.Box(orientation=Gtk.Orientation.VERTICAL, spacing=6)
        window.set_child(main_box)

        # Создаем заголовок
        header = Gtk.Label(label="Выберите город для просмотра информации")
        header.set_margin_top(10)
        header.set_margin_bottom(10)
        main_box.append(header)

        # Создаем разделитель
        separator = Gtk.Separator(orientation=Gtk.Orientation.HORIZONTAL)
        main_box.append(separator)

        # Создаем контейнер для списка городов и текстового поля
        content_box = Gtk.Box(orientation=Gtk.Orientation.HORIZONTAL, spacing=6)
        main_box.append(content_box)

        # Создаем список городов
        self.city_list = Gtk.ListBox()
        self.city_list.connect("row-selected", self.on_city_selected)
        content_box.append(self.city_list)

        # Создаем текстовое поле для отображения информации о городе
        self.city_info = Gtk.Label()  # Заменяем Gtk.TextView на Gtk.Label для тестирования
        self.city_info.set_wrap(True)  # Включаем перенос текста
        content_box.append(self.city_info)

        # Добавляем города в список
        self.populate_city_list()

        window.present()

    def populate_city_list(self):
        cities = ["Москва", "Санкт-Петербург", "Казань", "Новосибирск", "Екатеринбург","Волгоград"]
        for city in cities:
            row = Gtk.ListBoxRow()
            label = Gtk.Label(label=city)
            row.set_child(label)
            self.city_list.append(row)

    def on_city_selected(self, listbox, row):
        if row:
            city = row.get_child().get_text()
            info = self.get_city_info(city)
            self.city_info.set_text(info)  # Используем set_text для Gtk.Label
